var mes = +prompt("Ingrese el mes en numeros");

switch (mes) {
    case 1:
        console.Log("Es enero");
        break;
    case 2:
        console.Log("Es febrero");
        break;
    case 3:
        console.Log("Es marzo");
        break;
    case 4:
        console.Log("Es abril");
        break;
    case 5:
        console.Log("Es mayo")
        break;

    default:
        console.Log("Error")
        break;
}
